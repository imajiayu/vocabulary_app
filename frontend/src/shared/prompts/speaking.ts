/** 评分维度、锚点、通用原则（三个 Part 共用） */
const SCORING_CRITERIA = `【评分维度】（按雅思官方标准，发音无法通过文本评估，不纳入评分）
1. 流利度与连贯性 (FC): 话语衔接、逻辑组织、话语标记词使用
2. 词汇资源 (LR): 词汇多样性、准确性、搭配地道程度
3. 语法广度与准确性 (GRA): 句型多样性、语法正确率、复杂结构运用

【评分锚点】（必须严格对照，最终分数为三项综合）
5.0: 频繁语法错误 + 词汇贫乏/大量错误 + 逻辑断裂
5.5: 较多错误 + 中式英语明显 + 连贯性差 + 难以持续表达
6.0: 有明显语法错误 + 词汇基础但不地道 + 能表达但组织欠佳
6.5: 少量错误 + 词汇准确但变化少 + 基本连贯
7.0: 偶有小错 + 词汇较准确自然 + 有衔接但句型偏简单
7.5: 语法基本正确 + 表达较地道 + 句型开始有变化
8.0: 语法正确 + 词汇地道多样 + 句型丰富 + 衔接自然
8.5+: 几乎无错 + 词汇精准丰富 + 复杂句型驾驭自如

【通用判分原则】
- 不确定时往低打，宁可严格
- 出现"Chinglish"搭配或不自然表达 → 分数封顶 6.5
- 表达地道但有 1-2 处语法错 → 7.0-7.5`

/** 输出格式（共用，严格 JSON） */
const OUTPUT_FORMAT_JSON = `【输出格式】严格返回以下 JSON 对象，不得输出任何其他字符、不得加 markdown 代码块：
{
  "score": 6.5,                   // 最终分数，数字，范围 4.0-9.0，以 0.5 为单位
  "capScore": 6.5,                // 由 capReasons 推导的分数上限（若无触发上限则与 score 相同）
  "capReasons": ["..."],          // 命中的上限条件描述（数组；未触发则空数组）
  "review": "中文点评...",        // 1-3 句中文，指出主要问题与优点
  "optimized": "改写后的英文..."  // 改写后的完整英文版本
}

【评分执行步骤】
1. 先按评分锚点给出一个"裸分"。
2. 遍历下方 Part 专属的【分数上限规则】，每命中一条就记录一个 capScore，取最低。
3. 最终 score = min(裸分, 所有命中的 capScore)。
4. 将命中的规则文本放入 capReasons 数组，帮助用户理解为什么被压分。`

